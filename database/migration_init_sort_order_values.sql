-- Міграція: ініціалізація значень sort_order для існуючих товарів
-- Дата: 2026-05-21
-- Призначення: присвоїти sort_order = порядковий номер за id ASC
--              для всіх товарів що мають sort_order = 0 (дефолт).
-- Запускати ОДИН РАЗ перед введенням drag-and-drop в адмінці.
-- ============================================================

SET @row := -1;

UPDATE products
SET sort_order = (@row := @row + 1)
ORDER BY id ASC;
